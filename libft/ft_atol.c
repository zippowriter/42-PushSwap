#include "libft.h"

long long	ft_atol(const char *str)
{
	int			i;
	long long	n;
	int			minus_check;

	i = 0;
	n = 0;
	minus_check = 1;
	while ((str[i] >= 9 && str[i] <= 13) || str[i] == 32)
		i++;
	if (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			minus_check = -1;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		n = n * 10 + (str[i] - '0');
		i++;
	}
	n = n * minus_check;
	return (n);
}
